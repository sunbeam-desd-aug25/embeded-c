#include<stdio.h>
/*
	Bitwise Opr
	- & | ^ << >> ~
	- output of biywise opr is int value

*/



int main()
{
	
	// Bitwise AND &
    // int res = 10.2 & 2.3 ;  // Bitwise opr not applicable for float and double

	int n1 = 10 , n2 = 5 , res;
	/*

	   10  :  0000 1010
	   5   :  0000 0101
	 -----------------------
	   &   :  0000 0000   => 0
	*/	
	res =  10 & 5 ;
	printf(" res= %d \n ", res );// 0

	/*

	   8   :  0000 1000
	   15  :  0000 1111
	 -----------------------
	   &   :  0000 1000   => 8
	*/
	printf("res = %d \n", 8 & 15);//8

	//-----------------------------------------------------------------
	//Bitwise  OR  |

	res = 10 | 5 ;
	/*

	   10  :  0000 1010
	   5   :  0000 0101
	 -----------------------
	   |   :  0000 1111   => 15
	*/

	printf("res = %d \n",res);//15
	/*

	   8   :  0000 1000
	   15  :  0000 1111
	 -----------------------
	   |   :  0000 1111   => 15
	*/
	printf("res = %d \n", 8 | 15); // 31

	//-------------------------------------------------
	//Bitwise Ex-or ^
	res = 10 ^ 5;
	/*

	   10  :  0000 1010
	   5   :  0000 0101
	 -----------------------
	   ^   :  0000 1111   => 15
	*/
	printf(" res= %d \n ", res );// 15
	/*

	   8   :  0000 1000
	   15  :  0000 1111
	 -----------------------
	   ^   :  0000 0111   => 7
	*/
	printf("res = %d \n", 8 ^ 15);//7


	//---------------------------------------------------	
	// Biwise <<   :leftshif
	//  num1  << n    :  num * (2 ^n)
    //   10   << 2   :   10  * (2 ^ 2)
	//               :   10  * 4
    //               :   40
	printf(" 10 << 2 : %d  \n ", 10 << 2);// 40
	//						   25 * ( 2 ^ 3 )
	printf(" 25 << 3 : %d  \n ", 25 << 3 ); // 200
	//						   15 * ( 2 ^ 4 )
	printf(" 15 << 4 : %d  \n ", 15 << 4 ); // 240


/*

	// Biwise >> Rightshift
	//  num1  >> n   :   num / (2 ^ n)
    //   10   >> 2   :   10  / (2 ^ 2)
	//               :   10  / 4
    //               :   2
	printf(" 10 >> 2 : %d  \n ", 10 >> 2);// 2
	printf(" 25 >> 3 : %d  \n ", 25 >> 3 ); // 
	//                           25 / ( 2 ^ 3 )
	//                           25 / 8 
	//                           3

	printf(" 15 >> 4 : %d  \n ", 15 >> 4 ); // 15 / 16
	                                        // 0 

 	//int res = 10.2 >> 2 ;  // error due to float
	
	res = 'A' >> '\n'; // 65 >> 10  
	//                    65 / (2 ^ 10)
	//                    65 / 1024
	//                    0
	printf(" 'A' >>  \\n : %d \n", res);//0
*/

	// Bitwise ~  1's Compliment
	//  ~n =  - ( n + 1 )
	//  ~n =  - ( 10 + 1)
	//     =  9

	printf("~10 : %d \n", ~10); // -11
	printf("~15 : %d \n", ~15); // -16


	printf("~-15 : %d \n", ~-15); // - ( -15 + 1 )
				     			 //  14

	return 0;
}














